/**
 * Dual AI Service — routes tasks to the right model:
 * - GPT-4o:           lead qualification, conversation extraction, live call coaching
 * - Claude Sonnet 4.6: deal analysis, analytics insights, deal brief, contract language, market trends
 */

const Anthropic = require('@anthropic-ai/sdk');
const OpenAI    = require('openai');

let anthropicClient = null;
let openaiClient    = null;

function getAnthropic() {
  if (!anthropicClient) {
    anthropicClient = new Anthropic({
      apiKey: process.env.ANTHROPIC_API_KEY,
      timeout: 55000,   // 55s — just under Railway's 60s request timeout
      maxRetries: 2,
    });
  }
  return anthropicClient;
}

function getOpenAI() {
  if (!openaiClient) {
    openaiClient = new OpenAI({ apiKey: process.env.OPENAI_API_KEY });
  }
  return openaiClient;
}

const AI_DISCLAIMER = '\n\n⚠️ This analysis was generated by AI and is not financial or legal advice. Verify all figures before making any offer.';

// ─── GPT-4o: Lead qualification scoring ──────────────────────────────────────
async function qualifyLead({ name, phone, propertyAddress, conversationHistory, motivationType }) {
  const openai = getOpenAI();
  const messages = [
    {
      role: 'system',
      content: `You are an expert real estate wholesale acquisitions AI. Analyze this seller conversation and score their motivation 0-100.

Scoring criteria:
- 80-100: Highly motivated — urgent timeline, financial pressure, distressed property
- 60-79: Motivated — ready to sell, open to offers
- 40-59: Somewhat motivated — considering selling
- 0-39: Low motivation — not serious or testing the market

Return ONLY valid JSON:
{
  "motivation_score": <number 0-100>,
  "motivation_factors": ["factor1", "factor2"],
  "recommended_action": "escalate_to_pipeline|continue_nurturing|archive",
  "seller_personality": "analytical|emotional|skeptical|motivated|resistant",
  "key_objections": ["objection1"],
  "estimated_timeline": "immediate|30days|60days|90days|unknown",
  "confidence": <number 0-100>
}`,
    },
    {
      role: 'user',
      content: `Seller: ${name}\nPhone: ${phone}\nProperty: ${propertyAddress}\nMotivation type: ${motivationType}\n\nConversation:\n${conversationHistory || 'No conversation yet — initial qualification.'}`,
    },
  ];

  const res = await openai.chat.completions.create({
    model: 'gpt-4o',
    messages,
    max_tokens: 400,
    temperature: 0.3,
    response_format: { type: 'json_object' },
  });

  return JSON.parse(res.choices[0].message.content);
}

// ─── GPT-4o: Extract deal terms from SMS conversation ─────────────────────────
async function extractDealTerms(conversationText) {
  const openai = getOpenAI();
  const res = await openai.chat.completions.create({
    model: 'gpt-4o',
    messages: [
      {
        role: 'system',
        content: `Extract real estate deal terms from this conversation. Return ONLY valid JSON:
{
  "offer_price": <number or null>,
  "contingencies": ["inspection", "financing", "appraisal"],
  "closing_date": "<ISO date string or null>",
  "special_terms": "<string or null>",
  "seller_asking_price": <number or null>,
  "requested_call": <boolean>,
  "call_requested_time": "<string or null>",
  "call_requested_timezone": "<string or null>",
  "next_action": "voice_call|send_offer|send_contract|schedule_followup|none"
}`,
      },
      { role: 'user', content: conversationText },
    ],
    max_tokens: 300,
    temperature: 0.1,
    response_format: { type: 'json_object' },
  });

  return JSON.parse(res.choices[0].message.content);
}

