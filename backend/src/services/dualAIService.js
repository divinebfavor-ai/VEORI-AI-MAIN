// ─── Claude Sonnet 4.6: Deal analysis ────────────────────────────────────────
async function analyzeDeal({ propertyAddress, arv, repairCosts, offerPrice, state, sellerMotivationScore, daysOnMarket, buyerPoolDepth }) {
  const anthropic = getAnthropic();
  const mao = (arv * 0.70) - repairCosts;
  const assignmentFeeEstimate = arv * 0.05;

  const msg = await anthropic.messages.create({
    model: 'claude-sonnet-4-6',
    max_tokens: 600,
    messages: [{
      role: 'user',
      content: `Analyze this real estate wholesale deal and return ONLY valid JSON:

Property: ${propertyAddress}
State: ${state}
ARV: $${arv?.toLocaleString()}
Repair Costs: $${repairCosts?.toLocaleString()}
MAO (70% rule): $${mao?.toLocaleString()}
Offer Price: $${offerPrice?.toLocaleString()}
Seller Motivation Score: ${sellerMotivationScore}/100
Days on Market: ${daysOnMarket || 'unknown'}
Buyer Pool Depth: ${buyerPoolDepth || 'unknown'}

Return JSON:
{
  "risk_score": <number 1-10, where 1=low risk, 10=high risk>,
  "risk_factors": ["factor1", "factor2"],
  "assignment_fee_range": {"min": <number>, "max": <number>},
  "offer_assessment": "above_mao|at_mao|below_mao|significantly_below_mao",
  "offer_warning": "<string or null>",
  "scenarios": {
    "conservative": {"profit": <number>, "probability": <number 0-100>},
    "moderate": {"profit": <number>, "probability": <number 0-100>},
    "aggressive": {"profit": <number>, "probability": <number 0-100>}
  },
  "recommendation": "<2-sentence recommendation>",
  "deal_velocity_estimate_days": <number>
}`,
    }],
  });

  const result = JSON.parse(msg.content[0].text);
  result._disclaimer = AI_DISCLAIMER;
  return result;
}

// ─── Claude Sonnet 4.6: Smart Deal Brief ─────────────────────────────────────
async function generateDealBrief({ deal, lastAiAction, sellerName, nextRecommendedStep }) {
  const anthropic = getAnthropic();

  const msg = await anthropic.messages.create({
    model: 'claude-sonnet-4-6',
    max_tokens: 400,
    messages: [{
      role: 'user',
      content: `Generate a 5-sentence deal brief for a real estate operator. Be concise and direct. No fluff.

Deal data:
- Property: ${deal.property_address}, ${deal.state}
- Stage: ${deal.stage} (since: ${deal.stage_changed_at ? new Date(deal.stage_changed_at).toLocaleDateString() : 'unknown'})
- Seller: ${sellerName || 'Unknown'}
- Motivation Score: ${deal.motivation_score || 'N/A'}/100
- Velocity Score: ${deal.deal_velocity_score || 'N/A'}%
- Last AI Action: ${lastAiAction || 'None recorded'}
- Risk Level: ${deal.risk_level || 'medium'}
- Offer: $${deal.offer_price?.toLocaleString() || 'None'} / MAO: $${deal.mao?.toLocaleString() || 'N/A'}

Write exactly 5 sentences:
1. Current stage and how long it has been there
2. Seller motivation score and what is driving it
3. Last AI action taken and when
4. Next recommended step
5. Risk level and any flags`,
    }],
  });

  return {
    brief: msg.content[0].text,
    disclaimer: 'This brief was generated by AI and is for informational purposes only.',
  };
}

